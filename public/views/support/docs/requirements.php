<div class="white-row">
    <legend>Requirements</legend>
    <p>
        In order to use kW Fusion, your server <strong class="red">must</strong> meet the following minimum requirements:
    <ul>
        <li>PHP version 5.5 or greater</li>
        <li>Apache server with the mod_rewrite module enabled</li>
        <li>A PDO compatible database (MySQL, PostgreSQL, SQLite, etc.)</li>
    </ul>
    </p>

    <p>
        The following is <strong class="green">optional</strong>, but highly recommended to enhance performance and scalability:
    <ul>
        <li>Memcached installed and running</li>
        <li>APC and / or Opcache modules enabled</li>
        <li>PHP gzip compression extension enabled</li>
    </ul>
    </p>
</div>